-- Setup all cron jobs for Unspendify
-- Run this in Supabase SQL Editor

-- 1. Scan emails every 15 minutes
SELECT cron.schedule(
  'scan-emails-periodic',
  '*/15 * * * *',
  $$
  SELECT net.http_post(
    url := current_setting('app.settings.supabase_url') || '/functions/v1/scan-emails',
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', 'Bearer ' || current_setting('app.settings.service_role_key')
    ),
    body := jsonb_build_object(
      'scanType', 'scheduled'
    )
  ) AS request_id;
  $$
);

-- 2. Schedule notifications daily at 9 AM
SELECT cron.schedule(
  'schedule-notifications-daily',
  '0 9 * * *',
  $$
  SELECT net.http_post(
    url := current_setting('app.settings.supabase_url') || '/functions/v1/schedule-notifications',
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', 'Bearer ' || current_setting('app.settings.service_role_key')
    ),
    body := '{}'::jsonb
  ) AS request_id;
  $$
);

-- 3. Send notifications every 5 minutes
SELECT cron.schedule(
  'send-notifications-periodic',
  '*/5 * * * *',
  $$
  SELECT net.http_post(
    url := current_setting('app.settings.supabase_url') || '/functions/v1/send-notifications',
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', 'Bearer ' || current_setting('app.settings.service_role_key')
    ),
    body := '{}'::jsonb
  ) AS request_id;
  $$
);

-- Verify all jobs
SELECT jobname, schedule, active FROM cron.job ORDER BY jobname;
